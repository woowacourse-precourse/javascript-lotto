const Lotto = require("../src/Lotto");

describe("로또 클래스 테스트", () => {
  test("로또 번호의 개수가 6개가 넘어가면 예외가 발생한다.", () => {
    expect(() => {
      new Lotto([[1, 2, 3, 4, 5, 6, 7]]);
    }).toThrow("[ERROR] 로또 번호는 6개여야 합니다.");
  });

  // TODO: 이 테스트가 통과할 수 있게 구현 코드 작성
  test("로또 번호에 중복된 숫자가 있으면 예외가 발생한다.", () => {
    expect(() => {
      new Lotto([[1, 2, 3, 4, 5, 5]]);
    }).toThrow("[ERROR] 로또 번호는 중복되면 안됩니다.");
  });

  test('로또 번호가 숫자로 이루어지지 않으면 예외가 발생한다.', () => {
    expect(() => {
      new Lotto([[1, 'a', 3, 4, 5, 6]]);
    }).toThrow('[ERROR] 로또 번호는 숫자로 이루어져야 합니다.')
  });

  test('로또 번호가 1에서 45사이의 숫자가 아니라면 예외가 발생한다. 1', () => {
    expect(() => {
      new Lotto([[0, 1, 2, 3, 4, 5]]);
    }).toThrow('[ERROR] 로또 번호는 1에서 45사이의 숫자여야 합니다.');
  })

  test('로또 번호가 1에서 45사이의 숫자가 아니라면 예외가 발생한다. 2', () => {
    expect(() => {
      new Lotto([[46, 1, 2, 3, 4, 5]]);
    }).toThrow('[ERROR] 로또 번호는 1에서 45사이의 숫자여야 합니다.');
  })
});
